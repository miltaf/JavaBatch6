package com.syntax.class08;

public class MultiplicationTable {

	public static void main(String[] args) {
		/*
		 * 
		 */

		int num=2;
				for (int i=1; i<=10; i++) {
					System.out.println(num+" * "+ i + " = "+i * num);
				}
	
				System.out.println("***********************************");
				
				int num1=3;
				for (int i=1; i>10; i--) {
					System.out.println(num1+" * "+ i + " = "+i * num1);
				}
	}

}
