package com.syntax.class08;

public class MultiplicationTable {

	public static void main(String[] args) {
		/*
		 * we want to create muptiplication table
		 * 
		 * 1X1=1 1X2=2 1X3=3
		 */

		int num = 4;

		for (int i=1 ; i <= 10; i++) {
			System.out.println(num + " * " + i + " = " + i * num);
		}
	}
}





