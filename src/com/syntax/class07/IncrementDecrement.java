package com.syntax.class07;

public class IncrementDecrement {

	public static void main(String[] args) {

		int x=10;
		x=x+1;
		x=x+1;
		x+=1;
		//another way to add +1 to a VARIABLE
		x++;
		System.out.println(x);
		
		int y=90;
		y-=1; //y=y-1;
		//another way to subtract -1 to a VARIABLE
		y--;
		System.out.print(y);
		
	}

}
